require 'httparty'

# Populate the graph with zeros
points = []
(1..10).each do |i|
  points << { x: i, y: rand(0) }
end
last_x = points.last[:x]

SCHEDULER.every '20s', :first_in => 0 do |job|
  result = HTTParty.get('http://www.fishbase.us/maintenance/FB/showXML.php?identifier=FB-2&ProviderDbase=03')

	if result['response']['taxon']['Kingdom'] == 'Animalia'
		out = 50
	else
		out = 0
	end
  
  	points.shift
	last_x += 1
	points << { x: last_x, y: out }
	
	send_event('graphfishbase', points: points)
end